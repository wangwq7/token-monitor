#!/usr/bin/env node
'use strict';

// Vendors the Cloudflare Worker's shared-code closure into worker/src/shared/.
// The "Deploy to Cloudflare" button isolates worker/ into a fresh repo, so the
// Worker may not import files above its own directory — anything outside the
// subdirectory makes the build fail:
//   https://developers.cloudflare.com/workers/platform/deploy-buttons/
// src/shared/ stays the single source of truth; run `npm run sync:worker` after
// editing it (CI re-runs this and fails on drift). See AGENTS.md.

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const SRC_DIR = path.join(ROOT, 'src', 'shared');
const DEST_DIR = path.join(ROOT, 'worker', 'src', 'shared');

// The Worker's import closure: index.js imports these three, and usage.js
// requires ./limits + ./history. All are Node-builtin-free and only reference
// each other, so this is the complete self-contained set.
const MODULES = ['limits.js', 'usage.js', 'history.js'];

const HEADER = '// @generated by scripts/sync-worker-shared.js — DO NOT EDIT.\n'
  + '// Edit src/shared/<name>.js instead, then run `npm run sync:worker`.\n\n';

fs.mkdirSync(DEST_DIR, { recursive: true });

for (const name of MODULES) {
  const src = fs.readFileSync(path.join(SRC_DIR, name), 'utf8');
  fs.writeFileSync(path.join(DEST_DIR, name), HEADER + src);
}

// worker/ is "type":"module", but the shared modules are CommonJS. This marker
// scopes them back to CommonJS so both Node and esbuild bundle them correctly.
fs.writeFileSync(
  path.join(DEST_DIR, 'package.json'),
  JSON.stringify({
    '//': 'Auto-generated by scripts/sync-worker-shared.js — scopes the vendored shared modules to CommonJS inside the ESM worker.',
    type: 'commonjs'
  }, null, 2) + '\n'
);

console.log(`Synced ${MODULES.length} shared modules → worker/src/shared/`);
